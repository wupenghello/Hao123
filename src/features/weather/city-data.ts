/** 按省份分组 */
export const provinceGroups: ProvinceGroup[] = (() => {
  const map = new Map<string, CityItem[]>()
  for (const city of allCities) {
    const list = map.get(city.province) || []
    list.push(city)
    map.set(city.province, list)
  }

  // 按特定顺序排列省份
  const order = [
    '北京', '上海', '天津', '重庆',
    '河北', '山西', '内蒙古', '辽宁', '吉林', '黑龙江',
    '江苏', '浙江', '安徽', '福建', '江西', '山东',
    '河南', '湖北', '湖南', '广东', '广西', '海南',
    '四川', '贵州', '云南', '西藏',
    '陕西', '甘肃', '青海', '宁夏', '新疆',
    '香港', '澳门', '台湾',
  ]

  return order
    .filter((p) => map.has(p))
    .map((name) => ({ name, cities: map.get(name)! }))
    .concat(
      [...map.entries()]
        .filter(([p]) => !order.includes(p))
        .map(([name, cities]) => ({ name, cities })),
    )
})()

/** 搜索城市 */
export function searchCities(keyword: string): CityItem[] {
  if (!keyword.trim()) return allCities
  const k = keyword.trim().toLowerCase()
  return allCities.filter(
    (c) =>
      c.name.toLowerCase().includes(k) ||
      c.province.toLowerCase().includes(k),
  )
}

/**
 * 按经纬度找最近的城市（Haversine 球面距离）
 * GPS 自动定位时和风 GeoAPI 不可用，用本地城市库反查最近城市名兜底
 */
export function nearestCity(lat: number, lng: number): CityItem {
  const toRad = (d: number) => (d * Math.PI) / 180
  const cosLat1 = Math.cos(toRad(lat))
  let best = allCities[0]
  let bestDist = Infinity
  for (const c of allCities) {
    const dLat = toRad(c.lat - lat)
    const dLng = toRad(c.lng - lng)
    const h =
      Math.sin(dLat / 2) ** 2 +
      cosLat1 * Math.cos(toRad(c.lat)) * Math.sin(dLng / 2) ** 2
    const dist = 2 * Math.atan2(Math.sqrt(h), Math.sqrt(1 - h))
    if (dist < bestDist) {
      bestDist = dist
      best = c
    }
  }
  return best
}
